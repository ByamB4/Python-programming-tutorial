a = 10
b = 5

print(a + b)            # нэмнэ
print(a - b)            # хасна
print(a * b)            # үржвэр
print(a / b)            # хуваана
print(a // b)           # бүхэлтэй хуваана
    
print(a % b)            # модуль
print(a > b)            # их
print(a < b)            # бага
print(a >= b)           # их бөгөөд тэнцүү
print(a <= b)           # бага бөгөөд тэнцүү
  
print(a ** b)           # зэрэг дэвшүүлнэ a ийн b зэрэг
