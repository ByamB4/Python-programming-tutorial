tutorial = "Python programming language"

print(tutorial.upper())                           # Том үсгээр хэвлэнэ
print(tutorial.lower())                           # Жижиг үсгээр хэвлэнэ
print(tutorial.count('programming'))              # 'programming' гэдэг үг хэд байгааг тоолж байна
print(tutorial.replace('Python', 'Java'))         # 'Python' үгийг 'Java' үгээр сольж байна
print(tutorial.index('language'))                 # 'language' үгийн хэддэх index г олно
